package com.xuefei.entity;

public class StuQuery {
	private String name;
	private String age;
	private String gender;
	private int beginservlet;
	private int endservlet;
	public int getBeginservlet() {
		return beginservlet;
	}
	public void setBeginservlet(int beginservlet) {
		this.beginservlet = beginservlet;
	}
	public int getEndservlet() {
		return endservlet;
	}
	public void setEndservlet(int endservlet) {
		this.endservlet = endservlet;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}	
}
